export const ALL_SKILLS = Array.from(
  new Set(Object.values(CATEGORY_SKILL_MAP).flat())
);

const CATEGORY_KEYS = Object.keys(CATEGORY_SKILL_MAP) as ProjectCategory[];

function normalizeCategory(category?: string | null): ProjectCategory | null {
  if (!category) return null;

  if (CATEGORY_KEYS.includes(category as ProjectCategory)) {
    return category as ProjectCategory;
  }

  const mapped = CATEGORY_VALUES[category];
  if (mapped && CATEGORY_KEYS.includes(mapped)) {
    return mapped;
  }

  return null;
}

export function getSkillsByCategory(category?: string | null): string[] {
  const normalized = normalizeCategory(category);
  if (normalized && CATEGORY_SKILL_MAP[normalized]) {
    return CATEGORY_SKILL_MAP[normalized];
  }
  return ALL_SKILLS;
}

